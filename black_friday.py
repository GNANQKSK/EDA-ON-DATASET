# ======================================
# 1. IMPORT LIBRARIES
# ======================================
import pandas as pd
import matplotlib.pyplot as plt

# ======================================
# 2. UPLOAD DATASET (COLAB)
# ======================================
from google.colab import files
uploaded = files.upload()

df = pd.read_csv(list(uploaded.keys())[0])

# ======================================
# 3. BASIC DATA EXPLORATION
# ======================================
print("Shape of Dataset:", df.shape)
print("\nColumns:\n", df.columns)
print("\nData Types:\n", df.dtypes)
print("\nMissing Values:\n", df.isnull().sum())
print("\nStatistical Summary:\n", df.describe())

# ======================================
# 4. TOTAL & AVERAGE SALES
# ======================================
total_sales = df["Purchase"].sum()
avg_sales = df["Purchase"].mean()

print("\nTotal Sales:", total_sales)
print("Average Sales:", avg_sales)

# ======================================
# 5. PURCHASE DISTRIBUTION
# ======================================
plt.figure()
plt.hist(df["Purchase"], bins=20)
plt.title("Purchase Amount Distribution")
plt.xlabel("Purchase Amount")
plt.ylabel("Frequency")
plt.show()

# ======================================
# 6. GENDER VS SALES
# ======================================
gender_sales = df.groupby("Gender")["Purchase"].mean()

plt.figure()
gender_sales.plot(kind="bar")
plt.title("Average Purchase by Gender")
plt.xlabel("Gender")
plt.ylabel("Average Purchase")
plt.show()

# ======================================
# 7. AGE GROUP VS SALES
# ======================================
age_sales = df.groupby("Age")["Purchase"].mean()

plt.figure()
age_sales.plot(kind="bar")
plt.title("Average Purchase by Age Group")
plt.xlabel("Age Group")
plt.ylabel("Average Purchase")
plt.show()

# ======================================
# 8. CITY CATEGORY VS SALES
# ======================================
city_sales = df.groupby("City_Category")["Purchase"].mean()

plt.figure()
city_sales.plot(kind="bar")
plt.title("Average Purchase by City Category")
plt.xlabel("City Category")
plt.ylabel("Average Purchase")
plt.show()

# ======================================
# 9. MARITAL STATUS VS SALES
# ======================================
marital_sales = df.groupby("Marital_Status")["Purchase"].mean()

plt.figure()
marital_sales.plot(kind="bar")
plt.title("Average Purchase by Marital Status")
plt.xlabel("Marital Status (0 = Single, 1 = Married)")
plt.ylabel("Average Purchase")
plt.show()

# ======================================
# 10. OCCUPATION VS SALES (TOP 10)
# ======================================
occupation_sales = df.groupby("Occupation")["Purchase"].mean().sort_values(ascending=False).head(10)

plt.figure()
occupation_sales.plot(kind="bar")
plt.title("Top 10 Occupations by Average Purchase")
plt.xlabel("Occupation")
plt.ylabel("Average Purchase")
plt.show()

# ======================================
# 11. PRODUCT CATEGORY 1 VS SALES
# ======================================
prod_cat1_sales = df.groupby("Product_Category_1")["Purchase"].mean()

plt.figure()
prod_cat1_sales.plot(kind="bar")
plt.title("Average Purchase by Product Category 1")
plt.xlabel("Product Category 1")
plt.ylabel("Average Purchase")
plt.show()

# ======================================
# 12. STAY IN CURRENT CITY VS SALES
# ======================================
stay_sales = df.groupby("Stay_In_Current_City_Years")["Purchase"].mean()

plt.figure()
stay_sales.plot(kind="bar")
plt.title("Average Purchase by Stay in Current City")
plt.xlabel("Years in Current City")
plt.ylabel("Average Purchase")
plt.show()

# ======================================
# 13. SALES CONTRIBUTION BY GENDER (PIE CHART)
# ======================================
gender_total_sales = df.groupby("Gender")["Purchase"].sum()

plt.figure()
gender_total_sales.plot(kind="pie", autopct="%1.1f%%")
plt.title("Total Sales Contribution by Gender")
plt.ylabel("")
plt.show()

# ======================================
# 14. TOP 10 HIGHEST PURCHASES
# ======================================
print("\nTop 10 Highest Purchases:\n")
print(df[["User_ID", "Product_ID", "Purchase"]].sort_values(by="Purchase", ascending=False).head(10))

# ======================================
# 15. CORRELATION WITH PURCHASE
# ======================================
numeric_df = df.select_dtypes(include=["int64", "float64"])

print("\nCorrelation with Purchase:\n")
print(numeric_df.corr()["Purchase"].sort_values(ascending=False))